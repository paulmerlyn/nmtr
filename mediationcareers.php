<h1>How to Become a Mediator</h1>
<p>To become a mediator, begin with a 40-hour basic mediation training class. For hands-on experience in conducting the mediation process, look for a mediation class that includes several role plays. But one or even several classes will not prepare you sufficiently for professional mediation. You&rsquo;ll need to accumulate real case experience.</p>

<p>Fortunately, community mediation agencies are now widespread, and these agencies recruit and depend upon an army of volunteer mediators to service their caseload. Completion of a basic 40-hour mediation training and perhaps an in-person interview are usually the only requirements to join a community mediation agency&rsquo;s panel of mediators.</p>

<p>Your local courthouse might provide another source from which to accumulate mediation experience. Many states and municipalities have  mediation programs &mdash; often staffed by volunteer mediators &mdash; for small claims, fair housing, victim-offender, and family cases. Whereas a few programs are restricted to attorneys or licensed mental health professionals, others have lesser requirements.</p>

<p>When seeking case experience, take as many cases as you can. Of course, it&rsquo;s best to gain experience in a variety of case types (child custody, neighbor-to-neighbor, tenant-landlord, homeowner association, etc.), but don&rsquo;t worry if that proves impracticable. If you keep your eyes and ears open, you should find opportunities to fortify your mediation skills.</p>

<p>For additional help in gaining mediation experience, ask for suggestions when you join a regional mediation association or attend association events. Ask mediation trainers too. And search Google, Yahoo!, and Bing using keywords like <kbd>community mediation [your city]</kbd>, <kbd>volunteer mediators [your city]</kbd>, and <kbd>alternative dispute resolution [your city]</kbd>.</p>

<p>If you&rsquo;re still hungry for more case experience, form your own mediation study group. You only need a few like-minded individuals &mdash; perhaps a cadre of students from a class you recently attended. Volunteer to host weekly or monthly meetings or rendezvous at a local coffee shop. Spend some time talking about the theory and practice of mediation, but devote the majority of the meeting to mediation role plays. Of course, you&rsquo;ll need to create mediation case scenarios and take turns to play parties and mediators. To get  started, you&rsquo;ll find one such <a href="http://www.mediationcareer.org/mediationroleplay.php">mediation role-play</a> here. I wrote it for a study group I attended several years ago.</p>

<p>Finally, some shameless self-promotion: For specific advice on setting up and operating a mediation practice, visit my personal <a href="http://www.mediationcareer.org/mediatormentoring.php">mediator mentoring</a> and <a href="http://www.mediationcareer.org">mediator career coaching</a> site. Here you&rsquo;ll find not only career resources for purchase but also many free tips and pointers and a link to my <a href="http://mediationcareer.org/careermediator/">mediation blog</a>.</p>

