<h1>Mediation Training Role-Play</h1>

<h2>Instructions for Mediators</h2>

<p>Party 1 (P1) is Alan/Elaine Turin<br />
Party 2 (P2) is Stephanie/Steven Chandler, Esq.</p>

<p>P1 is an architect who is alleging that P2 owes him/her money after P2 failed to secure an immigrat&shy;ion visa for his/her employee, Chulho Park.</p>

<h2>Instructions for P1</h2>

<h3>ALAN/ELAINE TURIN</h3>

<p>[To be read by P1 only]</p>

<p>You&rsquo;re an architect. You present with a gentle personality. Six months ago, you retained the services of Stephanie/Steven Chandler, Esq.&mdash; a young San Francisco immigration attorney &mdash; to secure an H1-B employment visa for Chulho Park, a talented Korean designer whom you believed would be critical to your company&rsquo;s bid for the Ferris Building contract. Although your company is small, the Ferris Corporation has several times commissioned smaller architectural firms for new construction and renovation projects worldwide.</p>

<p>You paid Mr./Ms. Chandler a $15,000 retainer some six months ago. All seemed to be progressing satisfactorily for the first few months as Mr./Ms. Chandler sent you regular itemized statements to reflect the hours expended on the case. Then Mr./Ms. Chandler called to say that U.S. Citizenship and Immigration Services [formerly, the Immigration and Naturalization Service, INS] had announced that it had exhausted its H1-B visa allocation for the year. Mr./Ms. Chandler offered to refund you the unused balance ($4700) of the retainer. Enraged at your attorney&rsquo;s failure to secure a visa for Chulho Park, you argued that Mr./Ms. Chandler should refund much more money, at least $10,000. You might have said something threatening (you can&rsquo;t remember exactly what you said). Certainly, you have some power to damage his/her reputation because you&rsquo;re influential in the San Francisco Chamber of Commerce.</p>

<p>In your industry, the service provider takes responsibility for failure to deliver. And even if Mr./Ms. Chandler is technically right about the hours he/she worked on the case, it&rsquo;s still professionally appropriate to bear a fair share of the loss.</p>

<p>You requested mediation because you felt litigation would likely end with a judge siding with a fellow lawyer. Right now, you&rsquo;d like an apology and at least the $5000 back from Mr./Ms. Chandler. Principles are important to you.</p>

<p><strong>Information to reveal only if the mediators make you feel comfortable:</strong> Although you suspect Mr./Ms. Chandler bungled the H1-B visa application, Chulho Park returned to Korea regardless of the visa outcome. Apparently, he got offered an associate professorship at Seoul University. You&rsquo;re actually quite angry with him because he always told you he wanted to remain in San Francisco long term, and you feel somewhat betrayed after spending quite a lot of money and effort on trying to secure his visa.</p>

<p><strong>Other character notes:</strong> You&rsquo;re a Jekyll-and-Hyde: pleasant, calm, and softly spoken, but also revealing glimpses of a very angry and aggressive facet of your personality. Play the character according to your instincts. Yield only when you feel you&rsquo;ve gotten what you need.</p>

<h2>Instructions for P2</h2>

<h3>STEPHANIE/STEVEN CHANDLER, ESQ.</h3>

<p>[To be read by P2 only]</p>

<p>You&rsquo;re an immigration attorney in San Francisco. You accepted a case about six months ago from an architect (Alan/Elaine Turin) who needed to secure an H1-B visa for a Korean employee. You received a $15,000 retainer from Mr./Ms. Turin and consumed $10,300 in fees while working on the case. You submitted the visa application ahead of the deadline, and then, to your surprise, received an announcement from the Center for Immigration Services [formerly, the Immigration and Naturalization Service, INS] that the H1-B visa allocation for the year was now exhausted. No new H1-B visas would be issued until next year.</p>

<p>You called Mr./Ms. Turin to break the bad news. He/she was clearly angry &mdash; excessively so, in your opinion. You ended the call by saying that you&rsquo;d mail him/her a refund for the unused portion of the retainer.</p>

<p>When Mr./Ms. Turin received your check for $4700, he/she called you in a state of rage, threatening to &ldquo;hound you&rdquo; unless you &ldquo;take responsibility and repay the money owed&rdquo;. Several more calls followed with Mr./Ms. Turin demanding various sums of money, all in excess of the $4700. You tried to reason with him/her, hoping to avoid a complaint to the Bar Association that might damage your as-yet untarnished reputation. You also know that Mr./Ms. Turin is also quite influential on the San Francisco Chamber of Commerce, and you&rsquo;re concerned about having your name dragged through the mud with a constituency of business executives on whom you depend for much of your clientele.</p>

<p><strong>Information to reveal only if the mediators make you feel comfortable:</strong> You&rsquo;ve always been fascinated by architecture and civil engineering, and you&rsquo;re thrilled that your son, who had a misspent youth, to put it mildly &mdash; has just started to straighten up his act. In fact, you&rsquo;re thrilled that he&rsquo;s now enrolled in architectural school. You were even hoping that this client could help him find an intern position during the summer. You don&rsquo;t much enjoy the practice of law and don&rsquo;t care for attorneys, and you&rsquo;d rather talk about architecture than immigration visas!</p>

<p><strong>Other character notes:</strong> Deep down, you suspect Mr./Ms. Turin is a nice person. Like a lot of artist types, he/she is emotional and perhaps doesn&rsquo;t understand business realities. You&rsquo;d like to find a way through this mess. You won&rsquo;t tolerate being bullied, and you want some assurance your name isn&rsquo;t going to be slandered in the Chamber of Commerce.</p>

